integer_one = int(input("Enter integer one: "))
integer_two = int(input("Enter integer two: "))

if integer_one == integer_two:
    print(integer_one, "is equal to", integer_two)
else: print(integer_one, "is not equal to", integer_two)
if integer_one < integer_two:
    print(integer_one, "is less than", integer_two)
else: print(integer_one, "is greater than", integer_two)
if integer_one <= integer_two:
    print(integer_one, "is less than or equal to", integer_two)
else: print(integer_one, "is greater than or equal to", integer_two)  
